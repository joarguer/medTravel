function notification(text,title,status){
    toastr.options = {
        "closeButton": true,
        "debug": false,
        "positionClass": "toast-top-right",
        "onclick": null,
        "showDuration": "1000",
        "hideDuration": "1000",
        "timeOut": "5000",
        "extendedTimeOut": "1000",
        "showEasing": "swing",
        "hideEasing": "linear",
        "showMethod": "fadeIn",
        "hideMethod": "fadeOut",
        "background": "#000",
        "color": "#000",
        "borderRadius": "10px",
        "progressBar": true
    }
    if(status == "success"){
      toastr.success(text,title)
    } else{
      toastr.error(text,title)
    }
}